package com.github.artsydev;


public class CastMember {

    private final String animal;
    private final String rhyme;

    public CastMember(String animal, String rhyme) {
        this.animal = animal;
        this.rhyme = rhyme;
    }

    public String getAnimal() {
        return animal;
    }

    public String getRhyme() {
        return rhyme;
    }
}
